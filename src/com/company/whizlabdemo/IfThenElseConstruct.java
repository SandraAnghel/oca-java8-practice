package com.company.whizlabdemo;

/**
 *  - the if-then-else construct provides a secondary path of execution for if-then when the if clause evaluates to false
 */
public class IfThenElseConstruct {
    public static void main(String[] args) {
        int i = -1;
        if(i > 0) {
            System.out.println("Positive");
        }else {
            System.out.println("Non-positive");
        }
    }
}
